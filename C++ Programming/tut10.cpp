#include<iostream>

using namespace std;

int main()
{
    for( int i = 1; i <= 40; i++)
    {
        cout<<i<<endl;
    }
    
    int a=1;
    while (a<=40)
    {
        cout<<a<<endl;
        a++;
    }

    int b =1;
    do
    {
            cout<<b<<endl;
            b++; 
    } while (b<=40);
    
    return 0;
}